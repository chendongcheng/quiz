def build():
    if len(QUESTIONS) != 300:
        raise SystemExit(f"Expected 300 questions, got {len(QUESTIONS)}")

    for idx, wrongs in HARD_WRONG_OVERRIDES.items():
        QUESTIONS[idx - 1]["wrongs"] = wrongs
    for idx, wrongs in MEDIUM_WRONG_OVERRIDES.items():
        QUESTIONS[idx - 1]["wrongs"] = wrongs
    for idx, question in FINAL_HARD_QUESTION_OVERRIDES.items():
        QUESTIONS[idx - 1]["question"] = question
    for idx, question in FINAL_QUESTION_TEXT_OVERRIDES.items():
        QUESTIONS[idx - 1]["question"] = question
    for idx, correct in FINAL_CORRECT_OVERRIDES.items():
        QUESTIONS[idx - 1]["correct"] = correct
    for idx, wrongs in FINAL_HARD_WRONG_OVERRIDES.items():
        QUESTIONS[idx - 1]["wrongs"] = wrongs
    for idx, wrongs in FINAL_MEDIUM_WRONG_OVERRIDES.items():
        QUESTIONS[idx - 1]["wrongs"] = wrongs
    for idx, question in REVIEW_QUESTION_OVERRIDES.items():
        QUESTIONS[idx - 1]["question"] = question
    for idx, correct in REVIEW_CORRECT_OVERRIDES.items():
        QUESTIONS[idx - 1]["correct"] = correct
    for idx, wrongs in REVIEW_WRONG_OVERRIDES.items():
        QUESTIONS[idx - 1]["wrongs"] = wrongs
    for idx, explanation in REVIEW_EXPLANATION_OVERRIDES.items():
        QUESTIONS[idx - 1]["explanation"] = explanation

    question_counts = Counter(item["question"] for item in QUESTIONS)
    duplicates = [text for text, count in question_counts.items() if count > 1]
    if duplicates:
        raise SystemExit(f"Duplicate question_text detected: {duplicates[:5]}")

    rows = []
    keys = ["A", "B", "C", "D"]
    for idx, item in enumerate(QUESTIONS, start=1):
        if len(item["wrongs"]) != 3:
            raise SystemExit(f"Question {idx} must have exactly 3 wrong options")
        option_texts = [item["correct"], *item["wrongs"]]
        if len(set(option_texts)) != 4:
            raise SystemExit(f"Question {idx} has duplicated options")

        correct_position = (idx - 1) % 4
        ordered = item["wrongs"][:]
        ordered.insert(correct_position, item["correct"])
        options = [{"key": key, "text": text} for key, text in zip(keys, ordered)]
        correct_option = keys[correct_position]
        source = SOURCE_BY_CATEGORY[item["category"]]
        fingerprint = hashlib.md5(
            f"{item['category']}|{item['question']}|{item['correct']}".encode("utf-8")
        ).hexdigest()
        level = item["level"]
        risk_level = 2 if item["category"] in {"用药安全", "慢病预防"} or level == 3 else 1
        rows.append(
            {
                "id": idx,
                "status": "ENABLED",
                "category": item["category"],
                "tags_json": item["tags"],
                "use_count": 0,
                "created_at": CREATED_AT,
                "difficulty": "EASY" if level == 1 else "NORMAL",
                "risk_level": risk_level,
                "updated_at": CREATED_AT,
                "explanation": item["explanation"],
                "source_text": source,
                "source_type": SOURCE_TYPE,
                "wrong_count": 0,
                "options_json": options,
                "correct_count": 0,
                "quality_score": {1: 86, 2: 89, 3: 92}[level],
                "question_text": item["question"],
                "correct_option": correct_option,
                "difficulty_level": level,
                "reference_source": source,
                "semantic_fingerprint": fingerprint,
            }
        )

    fingerprint_counts = Counter(row["semantic_fingerprint"] for row in rows)
    if any(count > 1 for count in fingerprint_counts.values()):
        raise SystemExit("Duplicate semantic_fingerprint detected")

    with open(OUTPUT, "w", encoding="utf-8") as f:
        json.dump(rows, f, ensure_ascii=False, indent=2)
        f.write("\n")

    print(f"Wrote {OUTPUT} with {len(rows)} questions")


if __name__ == "__main__":
    build()
